#include <stdio.h>

int main() {
    int a, b, c;
    
    // Taking input
    scanf("%d %d %d", &a, &b, &c);
    
    // Calculating average
    double average = (a + b + c) / 3.0;
    
    // Printing output with two decimal places
    printf("Average: %.2f\n", average);
    
    return 0;
}









