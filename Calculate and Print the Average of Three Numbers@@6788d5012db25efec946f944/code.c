#include <stdio.h>

int main() {
    int a, b, c;
    float avg;
    
    // Read three integers
    scanf("%d %d %d", &a, &b, &c);
    
    // Calculate the average
    avg = (a + b + c) / 3.0;
    
    // Print the average
    printf("%.2f", avg);

    return 0;
}









